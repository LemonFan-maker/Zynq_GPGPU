#!/usr/bin/env python3
"""
Export FC GPU bundle (.npz) to C header for gpu_app integration.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np


def format_c_array(name: str, arr: np.ndarray, c_type: str, values_per_line: int = 16) -> str:
    flat = arr.reshape(-1)
    lines = [f"const {c_type} {name}[{flat.size}] = {{"]
    for i in range(0, flat.size, values_per_line):
        chunk = flat[i : i + values_per_line]
        vals = ", ".join(str(int(v)) for v in chunk)
        lines.append(f"    {vals},")
    lines.append("};")
    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(description="Export FC bundle to C header")
    parser.add_argument(
        "--bundle",
        type=Path,
        default=Path(__file__).resolve().parent / "gpu_export" / "fc_gpu_bundle.npz",
    )
    parser.add_argument(
        "--out-header",
        type=Path,
        default=Path(__file__).resolve().parents[2] / "gpu_app" / "src" / "mnist_fc_data.h",
    )
    parser.add_argument(
        "--max-samples",
        type=int,
        default=32,
        help="How many quantized test samples to emit into header",
    )
    args = parser.parse_args()

    d = np.load(args.bundle)

    tile = int(d["tile"][0])
    in_dim = int(d["in_dim"][0])
    hidden_dim = int(d["hidden_dim"][0])
    out_dim = int(d["out_dim"][0])
    bundle_samples = int(d["sample_count"][0])

    x_q = d["x_q"].astype(np.uint8)
    y = d["y"].astype(np.uint8)
    emit_samples = min(args.max_samples, x_q.shape[0], y.shape[0], bundle_samples)
    x_q = x_q[:emit_samples]
    y = y[:emit_samples]

    b1_q = d["b1_q"].astype(np.int32)
    b2_q = d["b2_q"].astype(np.int32)

    # Blocked int16 tensors are most direct for tiled GEMM path
    w1_blocks = d["w1_blocks"].astype(np.int16)
    w2_blocks = d["w2_blocks"].astype(np.int16)
    # Contiguous int8 tensors are faster for ARM baseline path
    w1_q = d["w1_q"].astype(np.int8)
    w2_q = d["w2_q"].astype(np.int8)

    x_scale = float(d["x_scale"][0])
    w1_scale = float(d["w1_scale"][0])
    w2_scale = float(d["w2_scale"][0])

    out = []
    out.append("// Auto-generated by datasets/mnist/export_fc_c_headers.py")
    out.append("#pragma once")
    out.append("#include <stdint.h>")
    out.append("")

    out.append(f"#define MNIST_FC_TILE {tile}")
    out.append(f"#define MNIST_FC_IN_DIM {in_dim}")
    out.append(f"#define MNIST_FC_HIDDEN_DIM {hidden_dim}")
    out.append(f"#define MNIST_FC_OUT_DIM {out_dim}")
    out.append(f"#define MNIST_FC_SAMPLE_COUNT {emit_samples}")
    out.append(f"#define MNIST_FC_W1_BLOCKS {w1_blocks.shape[0]}")
    out.append(f"#define MNIST_FC_W2_BLOCKS {w2_blocks.shape[0]}")
    out.append("")

    out.append(f"#define MNIST_FC_X_SCALE {x_scale:.10f}f")
    out.append(f"#define MNIST_FC_W1_SCALE {w1_scale:.10f}f")
    out.append(f"#define MNIST_FC_W2_SCALE {w2_scale:.10f}f")
    out.append("")

    out.append(format_c_array("mnist_fc_x_q", x_q, "uint8_t", values_per_line=24))
    out.append("")
    out.append(format_c_array("mnist_fc_y", y, "uint8_t", values_per_line=32))
    out.append("")
    out.append(format_c_array("mnist_fc_b1_q", b1_q, "int32_t", values_per_line=16))
    out.append("")
    out.append(format_c_array("mnist_fc_b2_q", b2_q, "int32_t", values_per_line=16))
    out.append("")
    out.append(format_c_array("mnist_fc_w1_q", w1_q, "int8_t", values_per_line=32))
    out.append("")
    out.append(format_c_array("mnist_fc_w2_q", w2_q, "int8_t", values_per_line=32))
    out.append("")
    out.append(format_c_array("mnist_fc_w1_blocks", w1_blocks, "int16_t", values_per_line=32))
    out.append("")
    out.append(format_c_array("mnist_fc_w2_blocks", w2_blocks, "int16_t", values_per_line=32))
    out.append("")

    args.out_header.parent.mkdir(parents=True, exist_ok=True)
    args.out_header.write_text("\n".join(out), encoding="utf-8")

    print(f"[ok] header generated: {args.out_header}")
    print(
        f"[info] samples={emit_samples}, x_q={x_q.shape}, w1_blocks={w1_blocks.shape}, w2_blocks={w2_blocks.shape}"
    )


if __name__ == "__main__":
    main()
